     void reset(){
         neg=false;
         jTextField1.setText("");
         
     }
     double cond(String s){
         return Double.parseDouble(s);
     }
     void eq(){
         double num= cond(jTextField1.getText());
         switch(func){
             case 1:
                 mem+=num;
                 break;
             case 2:
                 mem-=num;
                 break;
             case 3:
                 mem*=num;
                 break;
             case 4:
                 mem/=num;
                 break;
             case 5:
                 mem%=num;
                 break;
             default:
                 break;
                 
         }
    }
    
    void button(int n , String s){
        if(!jTextField1.getText().equals("") && !start && !stat){
           mem=cond(jTextField1.getText());
       jLabel1.setText(jTextField1.getText()+" "+s);
       //jTextField1.setText("");
       func=n;
       start =true;
       stat=true;
       }
       else if(!jTextField1.getText().equals("") && !stat){
           eq();
           jLabel1.setText(Double.toString(mem)+" "+s);
           jTextField1.setText(Double.toString(mem));
           func=n;
           stat=true;
       }
       else{
         int length= jLabel1.getText().length();
       String s1= jLabel1.getText();
       String ss="";
       for(int i=0;i<length-1;i++)
           ss=ss+s1.charAt(i);
       jLabel1.setText(ss+s);
       func=n;
       }
        stat=true;
    } 
